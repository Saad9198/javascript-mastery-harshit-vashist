// Break Keyword
for(i=1; i<=10; i++) {
    if(i===4){
        break;// it will stop execution till the value is equal to 4 and jump out of Block
    }
    console.log(i);
}
console.log("hellow there");
// continue Keyword
for (let i2=1; i2<=10; i2++) {
    if(i2===5){
        continue;// it will skip 5 and execute all the number except 5
    }
    console.log(i2);
}
console.log("hellow there");


